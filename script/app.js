function toSignUp(){
var hideSignIn=document.getElementById("login");
var showSignup=document.getElementById("signup");
hideSignIn.style.display="none";
showSignup.style.display="block";

}
function toSignIn(){
    var hideSignUp=document.getElementById("signup");
    var ShowLogin=document.getElementById("login");
    hideSignUp.style.display="none";
    ShowLogin.style.display="block";

}